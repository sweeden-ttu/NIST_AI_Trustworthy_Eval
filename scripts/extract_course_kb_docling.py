#!/usr/bin/env python3
"""
Extract course PDF and PPTX into knowledge_base/ using Docling.

Usage (from repo root):
  uv run --extra kb python scripts/extract_course_kb_docling.py
  uv run --extra kb python scripts/extract_course_kb_docling.py --force
"""

from __future__ import annotations

import argparse
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parents[1]
COURSE_DIR = REPO_ROOT / "scripts" / "course"
KB_ROOT = REPO_ROOT / "knowledge_base" / "course"
SOURCES: list[tuple[Path, str]] = [
    (COURSE_DIR / "AI Agent Evaluation.pdf", "ai_agent_evaluation.md"),
    (COURSE_DIR / "AgentEval-H-1.pptx", "agent_eval_h1.md"),
]


def main() -> None:
    p = argparse.ArgumentParser(description="Docling → knowledge_base/course/*.md")
    p.add_argument("--force", action="store_true", help="Re-convert even if output is newer than source")
    args = p.parse_args()

    try:
        from docling.document_converter import DocumentConverter
    except ImportError as e:  # pragma: no cover
        raise SystemExit(
            "docling is not installed. From repo root: uv sync --extra kb\n"
            f"Import error: {e}"
        ) from e

    KB_ROOT.mkdir(parents=True, exist_ok=True)
    converter = DocumentConverter()

    for src, out_name in SOURCES:
        if not src.is_file():
            raise SystemExit(f"Missing source file: {src}")
        out_path = KB_ROOT / out_name
        if (
            not args.force
            and out_path.is_file()
            and out_path.stat().st_mtime >= src.stat().st_mtime
        ):
            print(f"Skip (up to date): {out_path.relative_to(REPO_ROOT)}")
            continue
        print(f"Converting {src.name} …")
        result = converter.convert(str(src))
        md = result.document.export_to_markdown()
        header = f"<!-- source: {src.relative_to(REPO_ROOT)} | docling extract -->\n\n"
        out_path.write_text(header + md, encoding="utf-8")
        print(f"Wrote {out_path.relative_to(REPO_ROOT)} ({len(md):,} chars)")

    index = KB_ROOT / "INDEX.md"
    lines = [
        "# Course knowledge base (Docling)",
        "",
        "Generated by `scripts/extract_course_kb_docling.py`. Do not hand-edit without re-running.",
        "",
        "| File | Source |",
        "|------|--------|",
        "| [ai_agent_evaluation.md](./ai_agent_evaluation.md) | scripts/course/AI Agent Evaluation.pdf |",
        "| [agent_eval_h1.md](./agent_eval_h1.md) | scripts/course/AgentEval-H-1.pptx |",
        "",
    ]
    index.write_text("\n".join(lines) + "\n", encoding="utf-8")
    print(f"Wrote {index.relative_to(REPO_ROOT)}")


if __name__ == "__main__":
    main()
