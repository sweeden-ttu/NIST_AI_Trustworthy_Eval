#!/usr/bin/env python3
"""
Emit a LaTeX tabular fragment from nist_quiz_scores.json for pasting or \\input into main.tex.

Usage:
  python scripts/emit_nist_rubric_table.py
  python scripts/emit_nist_rubric_table.py --scores output/results/nist_quiz_scores.json
"""

from __future__ import annotations

import argparse
import json
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parents[1]
DEFAULT_SCORES = REPO_ROOT / "output" / "results" / "nist_quiz_scores.json"

NIST_FOCUS: dict[int, str] = {
    1: "Valid \\& Reliable",
    2: "Valid \\& Reliable",
    3: "Safe",
    4: "Safe",
    5: "Secure \\& Resilient",
    6: "Secure \\& Resilient",
    7: "Explainable \\& Interpretable",
    8: "Explainable \\& Interpretable",
    9: "Privacy-Enhanced",
    10: "Privacy-Enhanced",
    11: "Fair (bias managed)",
    12: "Fair (bias managed)",
    13: "Accountable \\& Transparent",
    14: "Accountable \\& Transparent",
}


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--scores", type=Path, default=DEFAULT_SCORES)
    ap.add_argument("--out", type=Path, default=REPO_ROOT / "output" / "results" / "nist_rubric_table.tex")
    args = ap.parse_args()

    data = json.loads(args.scores.read_text(encoding="utf-8"))
    scores = {int(k): v for k, v in data["scores"].items()}

    lines = [
        "% Auto-generated by scripts/emit_nist_rubric_table.py — do not edit by hand.",
        r"\begin{tabular}{@{}cll@{}}",
        r"\toprule",
        r"\textbf{ID} & \textbf{NIST characteristic} & \textbf{Rubric} \\",
        r"\midrule",
    ]
    for i in range(1, 15):
        ch = NIST_FOCUS[i]
        rubric = scores[i]
        lines.append(f"{i} & {ch} & \\textbf{{{rubric}}} \\\\")
    lines.extend(
        [
            r"\bottomrule",
            r"\end{tabular}",
            "",
        ]
    )
    args.out.parent.mkdir(parents=True, exist_ok=True)
    args.out.write_text("\n".join(lines), encoding="utf-8")
    print(f"Wrote {args.out}")


if __name__ == "__main__":
    main()
